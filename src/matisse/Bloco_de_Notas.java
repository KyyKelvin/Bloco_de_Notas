    private void checkEstiloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkEstiloActionPerformed
        String FontName = trcFonte.getSelectedItem().toString();
        int tamFont = (int) aumFont.getValue();
        
        if (checkEstilo.getSelectedItem().equals("PLAIN")) {
            Font newFont = new Font(FontName, Font.PLAIN, tamFont);
            txtPainel.setFont(newFont);
        }
        if (checkEstilo.getSelectedItem().equals("BOLD")) {
            Font newFont = new Font(FontName, Font.BOLD, tamFont); 
            txtPainel.setFont(newFont);
        }
        if (checkEstilo.getSelectedItem().equals("ITALIC")) {
            Font newFont = new Font(FontName, Font.ITALIC, tamFont);
            txtPainel.setFont(newFont);
        }
    }//GEN-LAST:event_checkEstiloActionPerformed

