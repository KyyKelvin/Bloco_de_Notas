    private void menSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menSaveActionPerformed
        JFileChooser diretorio = new JFileChooser();
        
        int result = diretorio.showSaveDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File arq = diretorio.getSelectedFile();
            System.out.println(arq.getPath());
            try {
                OutputStream os = new FileOutputStream(arq);
                OutputStreamWriter writer = new OutputStreamWriter(os);
                BufferedWriter buffer = new BufferedWriter(writer);

                buffer.write(txtPainel.getText());

                buffer.close();
                writer.close();
                os.close();
            } catch (IOException ex) {
                Logger.getLogger(Bloco_de_Notas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_menSaveActionPerformed

    private void btnOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpenActionPerformed
        JFileChooser file = new JFileChooser();

        int resposta = file.showOpenDialog(file);
        if (resposta == JFileChooser.APPROVE_OPTION) {
            File arquivo = file.getSelectedFile();
            System.out.println(arquivo.getPath());

            try {
                InputStream is = new FileInputStream(arquivo);
                InputStreamReader in = new InputStreamReader(is);
                BufferedReader buffer = new BufferedReader(in);
                String linha = buffer.readLine();

                while (linha != null) {
                    txtPainel.setText(txtPainel.getText() + linha);
                    linha = buffer.readLine();
                }
                txtPainel.setText(txtPainel.getText());
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Bloco_de_Notas.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Bloco_de_Notas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnOpenActionPerformed

